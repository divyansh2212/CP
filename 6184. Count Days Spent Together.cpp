#include <bits/stdc++.h>
using namespace std;
#define ll long long
#define inf INT_MAX
const int mod = 1e9 + 7;

class Solution
{
public:
    int countDaysTogether(string arriveAlice, string leaveAlice, string arriveBob, string leaveBob)
    {
        map<string, int> mp;
        int ans = 0;

        for (string i = arriveAlice; i <= leaveAlice;)
        {
            mp[i]++;
            if (i[0] == '0' && i[1] == '1' && i[3] == '3' && i[4] == '1')
                i[0] = '0', i[1] = '2', i[3] = '0', i[4] = '1';
            else if (i[0] == '0' && i[1] == '2' && i[3] == '2' && i[4] == '8')
                i[0] = '0', i[1] = '3', i[3] = '0', i[4] = '1';
            else if (i[0] == '0' && i[1] == '3' && i[3] == '3' && i[4] == '1')
                i[0] = '0', i[1] = '4', i[3] = '0', i[4] = '1';
            else if (i[0] == '0' && i[1] == '4' && i[3] == '3' && i[4] == '0')
                i[0] = '0', i[1] = '5', i[3] = '0', i[4] = '1';
            else if (i[0] == '0' && i[1] == '5' && i[3] == '3' && i[4] == '1')
                i[0] = '0', i[1] = '6', i[3] = '0', i[4] = '1';
            else if (i[0] == '0' && i[1] == '6' && i[3] == '3' && i[4] == '0')
                i[0] = '0', i[1] = '7', i[3] = '0', i[4] = '1';
            else if (i[0] == '0' && i[1] == '7' && i[3] == '3' && i[4] == '1')
                i[0] = '0', i[1] = '8', i[3] = '0', i[4] = '1';
            else if (i[0] == '0' && i[1] == '8' && i[3] == '3' && i[4] == '1')
                i[0] = '0', i[1] = '9', i[3] = '0', i[4] = '1';
            else if (i[0] == '0' && i[1] == '9' && i[3] == '3' && i[4] == '0')
                i[0] = '1', i[1] = '0', i[3] = '0', i[4] = '1';
            else if (i[0] == '1' && i[1] == '0' && i[3] == '3' && i[4] == '1')
                i[0] = '1', i[1] = '1', i[3] = '0', i[4] = '1';
            else if (i[0] == '1' && i[1] == '1' && i[3] == '3' && i[4] == '0')
                i[0] = '1', i[1] = '2', i[3] = '0', i[4] = '1';
            else if (i[4] == '9')
            {
                if (i[3] == '0')
                    i[3] = '1', i[4] = '0';
                else if (i[3] == '1')
                    i[3] = '2', i[4] = '0';
                else if (i[3] == '2')
                    i[3] = '3', i[4] = '0';
            }
            else
                i[4]++;
        }

        for (string i = arriveBob; i <= leaveBob;)
        {
            if (mp[i] == 1)
                ans++;
            if (i[0] == '0' && i[1] == '1' && i[3] == '3' && i[4] == '1')
                i[0] = '0', i[1] = '2', i[3] = '0', i[4] = '1';
            else if (i[0] == '0' && i[1] == '2' && i[3] == '2' && i[4] == '8')
                i[0] = '0', i[1] = '3', i[3] = '0', i[4] = '1';
            else if (i[0] == '0' && i[1] == '3' && i[3] == '3' && i[4] == '1')
                i[0] = '0', i[1] = '4', i[3] = '0', i[4] = '1';
            else if (i[0] == '0' && i[1] == '4' && i[3] == '3' && i[4] == '0')
                i[0] = '0', i[1] = '5', i[3] = '0', i[4] = '1';
            else if (i[0] == '0' && i[1] == '5' && i[3] == '3' && i[4] == '1')
                i[0] = '0', i[1] = '6', i[3] = '0', i[4] = '1';
            else if (i[0] == '0' && i[1] == '6' && i[3] == '3' && i[4] == '0')
                i[0] = '0', i[1] = '7', i[3] = '0', i[4] = '1';
            else if (i[0] == '0' && i[1] == '7' && i[3] == '3' && i[4] == '1')
                i[0] = '0', i[1] = '8', i[3] = '0', i[4] = '1';
            else if (i[0] == '0' && i[1] == '8' && i[3] == '3' && i[4] == '1')
                i[0] = '0', i[1] = '9', i[3] = '0', i[4] = '1';
            else if (i[0] == '0' && i[1] == '9' && i[3] == '3' && i[4] == '0')
                i[0] = '1', i[1] = '0', i[3] = '0', i[4] = '1';
            else if (i[0] == '1' && i[1] == '0' && i[3] == '3' && i[4] == '1')
                i[0] = '1', i[1] = '1', i[3] = '0', i[4] = '1';
            else if (i[0] == '1' && i[1] == '1' && i[3] == '3' && i[4] == '0')
                i[0] = '1', i[1] = '2', i[3] = '0', i[4] = '1';
            else if (i[4] == '9')
            {
                if (i[3] == '0')
                    i[3] = '1', i[4] = '0';
                else if (i[3] == '1')
                    i[3] = '2', i[4] = '0';
                else if (i[3] == '2')
                    i[3] = '3', i[4] = '0';
            }
            else
                i[4]++;
        }

        return ans;
    }
};